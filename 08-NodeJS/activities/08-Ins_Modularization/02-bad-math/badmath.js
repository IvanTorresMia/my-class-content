var add = function(num, num2) {
    return num + num2;
};

var product = function(num, num2) {
    return num * num2;
};

var devide = function(num, num2) {
    return num / num2;
}

var minus = function(num, num2) {
    return num - num2;
}

module.exports = {
    add,
    product,
    devide,
    minus
}